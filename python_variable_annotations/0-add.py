#!/usr/bin/env python3
"""
This module defines a function to add two floats.
"""


def add(a: float, b: float) -> float:
    """
    Function that adds two floats.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
